    def _build_document_context(self,
                               documents: list[Dict],
                               analysis_type: str) -> str:
        """
        Build context string for LLM from documents

        Args:
            documents: Documents to include
            analysis_type: Type of analysis (for formatting)

        Returns:
            Formatted context string
        """
        context_parts = []

        for i, doc in enumerate(documents):
            doc_id = doc.get('id', f'doc_{i}')
            text = doc.get('text', '')
            metadata = doc.get('metadata', {})

            # Limit text length for context
            max_chars = 5000
            if len(text) > max_chars:
                text = text[:max_chars] + "..."

            source = metadata.get('source', 'unknown')
            doc_header = f"\n--- Document {i+1}: {doc_id} (from {source}) ---\n"

            context_parts.append(doc_header + text)

        return "\n".join(context_parts)

    def estimate_tokens_for_analysis(self,
                                     documents: list[Dict],
                                     analysis_type: str) -> int:
        """
        Estimate tokens needed for analysis

        Args:
            documents: Documents to analyze
            analysis_type: Type of analysis

        Returns:
            Estimated token count
        """
        # Document text tokens
        doc_tokens = sum(
            len(doc.get('text', '').split()) * 1.3
            for doc in documents
        )

        # Add overhead for analysis type
        overhead = {
            "comparison": 2000,
            "synthesis": 2500,
            "extraction": 1500,
            "reasoning": 3000,
            "contradiction_detection": 2000,
            "evidence": 1500,
            "themes": 2000,
        }

        analysis_overhead = overhead.get(analysis_type, 2000)

        total = int(doc_tokens + analysis_overhead)

        return total

    def log_analysis(self, result: AnalysisResult):
        """Log analysis result for auditing"""
        self.analysis_history.append({
            'timestamp': datetime.now().isoformat(),
            'query': result.query,
            'analysis_type': result.analysis_type,
            'documents_analyzed': result.documents_analyzed,
            'token_usage': result.token_usage,
            'processing_time': result.processing_time,
        })

        logger.info(f"Analysis logged: {result.analysis_type} "
                   f"({result.documents_analyzed} docs, "
                   f"{result.token_usage} tokens)")

